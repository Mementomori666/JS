                            <h2 class="blogsubject">Juvenis scientia включен в каталог периодических изданий
                                Ulrich’s Periodicals Directory</h2>

                            <div class="blogdate">11/01/16</div>
         <span id="wb_uid14">Научный журнал &quot;Juvenis scientia&quot; включен в каталог периодических изданий Ульрих (Ulrich’s Periodicals Directory)<br>
Каталог Ульрих, впервые опубликованный в 1932 году, является авторитетным источником библиографической информации о более чем 300 000 периодических изданий всех типов на 200 языках и предоставляет сведения для различных реферативных баз данных и индексов цитирования, в том числе для Scopus и Web of Science. Базу данных Ульрих используют преподаватели и студенты академических библиотек, персонал и посетители публичных и специализированных библиотек, исследователи во всем мире.<br>
Доступ к каталогу осуществляется по подписке.</span></div>
