                            <h2 class="blogsubject">Присвоение DOI статьям</h2>

                            <div class="blogdate">15/03/16</div>
         <span id="wb_uid10">Теперь по желанию автора, статье может быть присвоен DOI.<br>
DOI (Digital object identifier), или цифровой идентификатор объекта - это стандарт обозначения предоставления информации в Интернете. Номер DOI всегда остается неизменным, и по нему с легкость можно найти журнал, статью или книгу. Это стандарт, с которым работают издательства со всего мира. Кроме того, мировое научное сообщество не признает электронные публикации без номера DOI, так как без него статья недостаточно стабильна в Интернете и может исчезнуть, что отрицательно сказывается на индексировании цитируемости.<br>
Поиск по номеру DOI осуществляется на сайтах International DOI Foundation (IDF) и CrossRef.</span></div>
