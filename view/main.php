Directory of Open Access Journals (DOAJ) – это директория, предлагающая свободный (бесплатный) доступ к свыше 1500 научных и академических журналов в электронном формате по всем направлениям. Цель директории состоит в продвижении и популяризация стремительно растущего количества научных журналов, доступных бесплатно через Интернет, а также в повышении роли международного движения “Журналы в открытом доступе” (Open Access Journals) и облегчении доступа к свободным электронным научным и исследовательским журналам.</span>
